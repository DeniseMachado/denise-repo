package org.academiadecodigo.stringrays.mapeditor;

public class Position {

    private int cols;
    private int rows;


    public Position(int cols, int rows) {
        this.cols = cols;
        this.rows = rows;
    }

    public int getCols() {
        return cols;
    }

    public int getRows() {
        return rows;
    }


}
